def kilometers_to_miles(value):
    return value / 1.609

def miles_to_kilometers(value):
    return value * 1.609

def years_to_minutes(value):
    return value * 365 * 24 * 60

def minutes_to_years(value):
    return value / (365 * 24 * 60)

def thousand_to_xkcd_thousand(value):
    return value + 24

